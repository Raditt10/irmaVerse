"use client";
import { useEffect, useState } from "react";
import { useRouter, useParams } from "next/navigation";
import { useSession } from "next-auth/react";
import DashboardHeader from "@/components/ui/Header";
import Sidebar from "@/components/ui/Sidebar";
import ChatbotButton from "@/components/ui/Chatbot";
import Toast from "@/components/ui/Toast";
import CartoonConfirmDialog from "@/components/ui/ConfirmDialog"; // Import Confirm Dialog
import Loading from "@/components/ui/Loading";
import {
  Calendar,
  MapPin,
  User,
  Clock,
  ArrowLeft,
  Mail,
  CheckCircle2,
  Sparkles,
  BookOpen,
  Target,
  MessageCircle,
  Tag,
  Share2,
  Users,
  CheckCircle,
  XCircle,
  History,
} from "lucide-react";

interface InviteDetail {
  id: string;
  email: string;
  name: string | null;
  avatar: string | null;
  status: string;
  createdAt: string;
}

interface Material {
  id: string;
  title: string;
  description: string;
  date: string;
  startedAt: string;
  location: string;
  instructor: string;
  instructorAvatar?: string;
  category: string;
  grade: string;
  thumbnailUrl?: string;
  isJoined?: boolean;
  points?: string[];
  attendedAt?: string;
  inviteDetails?: InviteDetail[];
}

const MaterialDetail = () => {
  const [material, setMaterial] = useState<Material | null>(null);
  const [loading, setLoading] = useState(true);
  const [showConfirmDelete, setShowConfirmDelete] = useState(false);

  const [toast, setToast] = useState<{
    show: boolean;
    message: string;
    type: "success" | "error";
  }>({ show: false, message: "", type: "success" });

  const router = useRouter();
  const params = useParams();
  const materialId = params.id as string;

  // 1. Session Check & Redirect
  const { data: session, status } = useSession({
    required: true,
    onUnauthenticated() {
      if (typeof window !== "undefined") {
        window.location.href = "/auth";
      }
    },
  });

  const role = session?.user?.role?.toLowerCase();
  const isPrivileged = role === "instruktur" || role === "admin" || role === "instructor";

  const showToast = (message: string, type: "success" | "error") => {
    setToast({ show: true, message, type });
    setTimeout(() => setToast((prev) => ({ ...prev, show: false })), 3000);
  };

  useEffect(() => {
    if (materialId) {
      fetchMaterialDetail();
    }
  }, [materialId]);

  const fetchMaterialDetail = async () => {
    try {
      setLoading(true);

      const res = await fetch(`/api/materials/${materialId}`);
      if (!res.ok) {
        if (res.status === 404) {
          throw new Error("Kajian tidak ditemukan");
        }
        const err = await res
          .json()
          .catch(() => ({ error: "Gagal mengambil data" }));
        throw new Error(err.error || "Gagal mengambil data kajian");
      }

      const data = await res.json();

      const mapped: Material = {
        id: data.id,
        title: data.title,
        description: data.description,
        date: data.date,
        startedAt: data.startedAt || "",
        location: data.location || "",
        instructor: data.instructor || (data.instructorName ?? "TBA"),
        instructorAvatar: data.instructorAvatar || null,
        category: data.category || "",
        grade: data.grade || "",
        thumbnailUrl: data.thumbnailUrl || null,
        isJoined: data.isJoined ?? false,
        points: data.points || [],
        attendedAt: data.attendedAt || undefined,
        inviteDetails: data.inviteDetails || [],
      };

      setMaterial(mapped);
    } catch (error: any) {
      console.error("Error loading material:", error);
      showToast(error.message || "Gagal memuat detail kajian", "error");
    } finally {
      setLoading(false);
    }
  };

  const handleDelete = async () => {
    try {
      const res = await fetch(`/api/materials/${materialId}`, {
        method: "DELETE",
      });

      if (!res.ok) {
        const error = await res.json();
        throw new Error(error.error || "Gagal menghapus kajian");
      }

      showToast("Kajian berhasil dihapus", "success");
      setTimeout(() => router.push("/materials"), 1500);
    } catch (error: any) {
      console.error("Delete Error:", error);
      showToast(error.message || "Terjadi kesalahan saat menghapus", "error");
    } finally {
      setShowConfirmDelete(false);
    }
  };

  const getCategoryBadge = (category: string) => {
    let style = "bg-slate-100 text-slate-700 border-slate-200";
    if (category === "Program Wajib")
      style = "bg-rose-100 text-rose-700 border-rose-200";
    if (category === "Program Ekstra")
      style = "bg-purple-100 text-purple-700 border-purple-200";
    if (category === "Program Next Level")
      style = "bg-amber-100 text-amber-700 border-amber-200";

    return (
      <div
        className={`flex items-center gap-1.5 px-3 py-1.5 rounded-xl text-xs font-black uppercase border-2 shadow-sm ${style}`}
      >
        <Tag className="w-3.5 h-3.5" strokeWidth={3} />
        {category}
      </div>
    );
  };

  // --- STATE LOADING ---
  if (status === "loading" || loading) {
    return (
      <div className="min-h-screen bg-[#FDFBF7]">
        <DashboardHeader />
        <div className="flex">
          <Sidebar />
          <div className="flex-1 flex flex-col items-center justify-center min-h-[calc(100vh-80px)]">
            <Loading text="Sedang memuat detail kajian..." size="lg" />
          </div>
        </div>
      </div>
    );
  }

  // --- STATE DATA NOT FOUND ---
  if (!material) {
    return (
      <div className="min-h-screen bg-[#FDFBF7]">
        <DashboardHeader />
        <div className="flex">
          <Sidebar />
          <div className="flex-1 p-8 flex flex-col items-center justify-center min-h-[calc(100vh-80px)]">
            <div className="w-24 h-24 bg-slate-100 rounded-full flex items-center justify-center border-4 border-dashed border-slate-300 mb-6">
              <Target className="h-10 w-10 text-slate-400" />
            </div>
            <h2 className="text-2xl font-black text-slate-700 mb-2">
              Kajian Tidak Ditemukan
            </h2>
            <p className="text-slate-500 mb-6">
              Mungkin kajian ini sudah dihapus atau ID-nya salah.
            </p>
            <button
              onClick={() => router.push("/materials")}
              className="mt-4 px-6 py-3 rounded-xl bg-teal-400 text-white font-black border-2 border-teal-600 border-b-4 hover:bg-teal-500 active:border-b-2 active:translate-y-0.5 transition-all"
            >
              Kembali ke Daftar
            </button>
          </div>
        </div>
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-[#FDFBF7]">
      <DashboardHeader />
      <div className="flex">
        <Sidebar />

        {/* Main Content Area */}
        <div className="flex-1 px-4 sm:px-6 lg:px-8 py-8 lg:py-12 w-full max-w-[100vw] overflow-x-hidden">
          <div className="max-w-6xl mx-auto space-y-6 lg:space-y-8">
            {/* Back Button */}
            <button
              onClick={() => router.push("/materials")}
              className="inline-flex items-center gap-2 text-slate-500 hover:text-teal-600 font-bold transition-all group px-4 py-2 rounded-xl border-2 border-transparent hover:border-slate-200 hover:bg-white hover:shadow-sm"
            >
              <ArrowLeft className="h-5 w-5 group-hover:-translate-x-1 transition-transform stroke-3" />
              Kembali
            </button>

            {/* --- HERO SECTION --- */}
            <div className="relative bg-[#334155] rounded-[40px] md:rounded-[60px] border-4 border-slate-200 shadow-[0_12px_0_0_#cbd5e1] overflow-hidden min-h-[300px] md:min-h-[400px] flex flex-col justify-end p-8 md:p-12 group">
              {/* Image with Gradient Overlay */}
              <div className="absolute inset-0">
                <img
                  src={material.thumbnailUrl || "https://picsum.photos/1200/600"}
                  alt={material.title}
                  className="w-full h-full object-cover group-hover:scale-105 transition-transform duration-700 opacity-40 brightness-50"
                />
                <div className="absolute inset-0 bg-gradient-to-t from-[#1e293b] via-[#334155]/60 to-transparent" />
              </div>

              <div className="relative z-10 w-full">
                <div className="mb-6 flex items-center gap-3">
                  <span className="px-5 py-2.5 rounded-full text-xs font-black bg-[#10b981] text-white border-2 border-[#059669] shadow-[0_4px_0_0_#065f46] uppercase tracking-wider animate-pulse">
                    SEGERA HADIR
                  </span>
                </div>
                
                <h1 className="text-4xl md:text-6xl lg:text-7xl font-black text-white mb-4 drop-shadow-xl leading-tight tracking-tight">
                  {material.title}
                </h1>
                
                <p className="text-slate-300 font-bold text-lg md:text-xl max-w-2xl leading-relaxed drop-shadow-md">
                  {material.description ? material.description.split('\n')[0] : "Deskripsi materi tidak tersedia..."}
                </p>
              </div>
            </div>

            {/* --- GRID LAYOUT --- */}
            <div className="grid grid-cols-1 lg:grid-cols-3 gap-8">
              {/* LEFT COLUMN (Details) */}
              <div className="lg:col-span-2 space-y-8">
                {/* Quick Stats Tiles */}
                <div className="grid grid-cols-1 md:grid-cols-3 gap-6">
                  {/* Tanggal */}
                  <div className="bg-white p-7 rounded-[35px] border-4 border-slate-200 shadow-[0_8px_0_0_#cbd5e1] flex flex-col items-center text-center hover:-translate-y-1 transition-all group">
                    <div className="w-14 h-14 rounded-2xl bg-rose-50 flex items-center justify-center mb-4 border-2 border-rose-100 group-hover:scale-110 transition-transform">
                      <Calendar className="h-7 w-7 text-rose-400" strokeWidth={2.5} />
                    </div>
                    <span className="text-[11px] text-rose-300 font-black uppercase tracking-[0.2em] mb-2">
                      Tanggal
                    </span>
                    <span className="text-slate-700 font-black text-lg">
                      {new Date(material.date).toLocaleDateString("id-ID", {
                        day: "numeric",
                        month: "short",
                        year: "numeric",
                      })}
                    </span>
                  </div>

                  {/* Waktu */}
                  <div className="bg-white p-7 rounded-[35px] border-4 border-slate-200 shadow-[0_8px_0_0_#cbd5e1] flex flex-col items-center text-center hover:-translate-y-1 transition-all group">
                    <div className="w-14 h-14 rounded-2xl bg-amber-50 flex items-center justify-center mb-4 border-2 border-amber-100 group-hover:scale-110 transition-transform">
                      <Clock className="h-7 w-7 text-amber-400" strokeWidth={2.5} />
                    </div>
                    <span className="text-[11px] text-amber-300 font-black uppercase tracking-[0.2em] mb-2">
                      Waktu
                    </span>
                    <span className="text-slate-700 font-black text-lg">
                      {material.startedAt} WIB
                    </span>
                  </div>

                  {/* Lokasi */}
                  <div className="bg-white p-7 rounded-[35px] border-4 border-slate-200 shadow-[0_8px_0_0_#cbd5e1] flex flex-col items-center text-center hover:-translate-y-1 transition-all group">
                    <div className="w-14 h-14 rounded-2xl bg-teal-50 flex items-center justify-center mb-4 border-2 border-teal-100 group-hover:scale-110 transition-transform">
                      <MapPin className="h-7 w-7 text-teal-400" strokeWidth={2.5} />
                    </div>
                    <span className="text-[11px] text-teal-300 font-black uppercase tracking-[0.2em] mb-2">
                      Lokasi
                    </span>
                    <span className="text-slate-700 font-black text-lg">
                      {material.location}
                    </span>
                  </div>
                </div>

                {/* Deskripsi Lengkap */}
                <div className="bg-white p-8 md:p-10 rounded-[45px] border-4 border-slate-200 shadow-[0_10px_0_0_#cbd5e1]">
                  <div className="flex items-center gap-5 mb-8">
                    <div className="w-14 h-14 bg-indigo-100 rounded-2xl border-4 border-indigo-200 flex items-center justify-center">
                      <Sparkles className="h-7 w-7 text-indigo-500" strokeWidth={3} />
                    </div>
                    <h2 className="text-3xl font-black text-slate-800 tracking-tight">
                      Detail Lengkap
                    </h2>
                  </div>

                  <p className="text-slate-600 font-bold leading-relaxed mb-8 whitespace-pre-line text-lg">
                    {material.description}
                  </p>

                  {material.points && material.points.length > 0 && (
                    <div className="bg-slate-50 p-6 rounded-3xl border-2 border-slate-100">
                      <h3 className="font-bold text-slate-700 mb-4 flex items-center gap-2">
                        <Target className="h-5 w-5 text-teal-500" />
                        Poin Pembahasan
                      </h3>
                      <ul className="space-y-3">
                        {material.points.map((point, idx) => (
                          <li
                            key={idx}
                            className="flex items-start gap-3 text-sm font-semibold text-slate-600"
                          >
                            <div className="w-1.5 h-1.5 rounded-full bg-teal-400 mt-2 shrink-0" />
                            {point}
                          </li>
                        ))}
                      </ul>
                    </div>
                  )}
                </div>
              </div>

              {/* RIGHT COLUMN (Instructor & CTA) */}
              <div className="space-y-6 lg:space-y-8">
                {/* Instructor Card */}
                <div className="bg-white rounded-[45px] border-4 border-slate-200 shadow-[0_10px_0_0_#cbd5e1] overflow-hidden p-8 flex flex-col items-center">
                  <div className="relative mb-6">
                    <div className="w-32 h-32 bg-teal-500 rounded-full border-4 border-white shadow-xl overflow-hidden">
                      {material.instructorAvatar ? (
                        <img
                          src={material.instructorAvatar}
                          alt={material.instructor}
                          className="w-full h-full object-cover"
                        />
                      ) : (
                        <div className="w-full h-full flex items-center justify-center text-white">
                          <User className="h-16 w-16" strokeWidth={2.5} />
                        </div>
                      )}
                    </div>
                    {/* Status Online indicator or similar if needed */}
                    <div className="absolute bottom-1 right-1 w-6 h-6 bg-green-400 border-4 border-white rounded-full shadow-sm" />
                  </div>

                  <h3 className="text-2xl font-black text-slate-800 leading-tight mb-2">
                    {material.instructor}
                  </h3>
                  
                  <div className="px-4 py-1.5 rounded-full bg-teal-50 text-teal-600 text-xs font-black uppercase border-2 border-teal-100 mb-8 self-center">
                    {material.category.replace("Program ", "")}
                  </div>

                  <div className="w-full pt-6 border-t-2 border-slate-100">
                    <p className="text-[11px] font-black text-slate-300 uppercase tracking-[0.2em] mb-4 text-center">
                      — HUBUNGI PEMATERI —
                    </p>
                    <button
                      onClick={() =>
                        router.push(
                          `/instructors/chat?name=${encodeURIComponent(material.instructor)}`,
                        )
                      }
                      className="w-full p-4 rounded-2xl bg-white border-2 border-slate-200 flex items-center justify-center gap-2 hover:border-teal-400 hover:bg-teal-50 transition-all group"
                    >
                      <MessageCircle className="w-5 h-5 text-teal-500 group-hover:scale-110 transition-transform" strokeWidth={3} />
                      <span className="font-bold text-slate-600">Kirim Pesan</span>
                    </button>
                  </div>
                </div>

                {/* Invite Status Card - Instructor Only */}
                {isPrivileged &&
                  material.inviteDetails &&
                  material.inviteDetails.length > 0 && (
                    <div className="bg-white rounded-5xl border-2 border-slate-200 shadow-[0_6px_0_0_#cbd5e1] overflow-hidden p-6 lg:p-8">
                      <h3 className="text-lg font-black text-slate-800 mb-1 flex items-center gap-2">
                        <Users className="w-5 h-5 text-amber-500" /> Status
                        Undangan
                      </h3>
                      <p className="text-xs text-slate-400 font-semibold mb-4">
                        {
                          material.inviteDetails.filter(
                            (i) => i.status === "accepted",
                          ).length
                        }{" "}
                        diterima ·{" "}
                        {
                          material.inviteDetails.filter(
                            (i) => i.status === "pending",
                          ).length
                        }{" "}
                        menunggu ·{" "}
                        {
                          material.inviteDetails.filter(
                            (i) => i.status === "rejected",
                          ).length
                        }{" "}
                        ditolak
                      </p>
                      <div className="space-y-2 max-h-64 overflow-y-auto">
                        {material.inviteDetails.map((inv) => {
                          const statusConfig: Record<
                            string,
                            {
                              label: string;
                              color: string;
                              icon: React.ReactNode;
                            }
                          > = {
                            pending: {
                              label: "Menunggu",
                              color:
                                "bg-amber-100 text-amber-700 border-amber-200",
                              icon: <Clock className="w-3.5 h-3.5" />,
                            },
                            accepted: {
                              label: "Diterima",
                              color:
                                "bg-emerald-100 text-emerald-700 border-emerald-200",
                              icon: <CheckCircle className="w-3.5 h-3.5" />,
                            },
                            rejected: {
                              label: "Ditolak",
                              color: "bg-red-100 text-red-700 border-red-200",
                              icon: <XCircle className="w-3.5 h-3.5" />,
                            },
                          };
                          const cfg =
                            statusConfig[inv.status] || statusConfig.pending;
                          return (
                            <div
                              key={inv.id}
                              className="flex items-center justify-between px-3 py-2.5 rounded-xl bg-slate-50 border border-slate-100 hover:bg-white transition-colors"
                            >
                              <div className="flex items-center gap-2.5 min-w-0">
                                {inv.avatar ? (
                                  <img
                                    src={inv.avatar}
                                    alt={inv.name || inv.email}
                                    className="w-8 h-8 rounded-full object-cover border-2 border-slate-200"
                                  />
                                ) : (
                                  <span className="w-8 h-8 flex items-center justify-center bg-slate-200 rounded-full text-slate-500 text-xs font-bold">
                                    {(inv.name || inv.email)
                                      .charAt(0)
                                      .toUpperCase()}
                                  </span>
                                )}
                                <div className="min-w-0">
                                  <p className="text-xs font-bold text-slate-700 truncate">
                                    {inv.name || inv.email}
                                  </p>
                                  {inv.name && (
                                    <p className="text-[10px] text-slate-400 truncate">
                                      {inv.email}
                                    </p>
                                  )}
                                </div>
                              </div>
                              <span
                                className={`inline-flex items-center gap-1 px-2 py-0.5 rounded-lg text-[10px] font-bold border shrink-0 ${cfg.color}`}
                              >
                                {cfg.icon} {cfg.label}
                              </span>
                            </div>
                          );
                        })}
                      </div>
                    </div>
                  )}

                {/* CTA / Action Box */}
                {isPrivileged ? (
                  <div className="bg-slate-50 rounded-5xl p-6 text-center border-2 border-slate-200 border-dashed">
                    <p className="text-slate-500 text-sm font-bold mb-4">
                      Kelola Kajian Ini
                    </p>
                    <div className="space-y-3">
                      <button
                        onClick={() =>
                          router.push(`/materials/${material.id}/attendance-list`)
                        }
                        className="w-full flex items-center justify-center gap-2 py-4 rounded-xl bg-teal-400 text-white font-black border-2 border-teal-600 border-b-4 hover:bg-teal-500 active:border-b-2 active:translate-y-0.5 transition-all shadow-sm group"
                      >
                        <History className="w-5 h-5 group-hover:rotate-12 transition-transform" />
                        Lihat Absensi
                      </button>
                      <div className="grid grid-cols-2 gap-3">
                        <button
                          onClick={() =>
                            router.push(`/materials/${material.id}/edit`)
                          }
                          className="w-full py-3 rounded-xl bg-white border-2 border-slate-200 text-slate-700 font-bold hover:border-amber-400 hover:text-amber-600 transition-all shadow-sm"
                        >
                          Edit
                        </button>
                        <button
                          onClick={() => setShowConfirmDelete(true)}
                          className="w-full py-3 rounded-xl bg-white border-2 border-slate-200 text-slate-700 font-bold hover:border-red-400 hover:text-red-600 transition-all shadow-sm"
                        >
                          Hapus
                        </button>
                      </div>
                    </div>
                  </div>
                ) : (
                  <div className="bg-linear-to-br from-teal-400 to-cyan-400 rounded-5xl p-6 lg:p-8 text-white border-2 border-teal-600 shadow-[0_6px_0_0_#0f766e] text-center relative overflow-hidden">
                    <div className="absolute top-0 right-0 w-32 h-32 bg-white/10 rounded-full -mr-10 -mt-10 blur-2xl"></div>

                    {material.attendedAt ? (
                      <>
                        <h3 className="text-2xl font-black mb-2 relative z-10">
                          Alhamdulillah! ✨
                        </h3>
                        <p className="text-teal-50 text-sm font-bold mb-6 leading-relaxed relative z-10">
                          Kamu sudah mengisi absensi untuk kajian ini. Semoga ilmunya berkah dan bermanfaat ya!
                        </p>
                        <div className="w-full py-4 rounded-2xl bg-white/20 text-white font-black border-2 border-white/30 backdrop-blur-sm flex items-center justify-center gap-2 relative z-10">
                          <CheckCircle2 className="w-5 h-5 text-white" />
                          Sudah Absen
                        </div>
                      </>
                    ) : (
                      <>
                        <h3 className="text-2xl font-black mb-2 relative z-10">
                          Siap Hadir?
                        </h3>
                        <p className="text-teal-50 text-sm font-bold mb-6 leading-relaxed relative z-10">
                          Jangan lupa isi absensi saat kegiatan berlangsung untuk
                          mencatat kehadiranmu.
                        </p>
                        <button
                          onClick={() =>
                            router.push(`/materials/${material.id}/absensi`)
                          }
                          className="w-full py-4 rounded-2xl bg-white text-teal-600 font-black border-2 border-teal-100 shadow-lg hover:bg-teal-50 hover:scale-[1.02] active:scale-[0.98] transition-all flex items-center justify-center gap-2 relative z-10"
                        >
                          <CheckCircle2 className="w-5 h-5" />
                          Aku Ikut! ✋
                        </button>
                      </>
                    )}
                  </div>
                )}
              </div>
            </div>
          </div>
        </div>
      </div>
      <ChatbotButton />

      {/* Toast Notification */}
      <Toast
        show={toast.show}
        message={toast.message}
        type={toast.type}
        onClose={() => setToast((prev) => ({ ...prev, show: false }))}
      />

      {/* Confirm Dialog */}
      <CartoonConfirmDialog
        isOpen={showConfirmDelete}
        onClose={() => setShowConfirmDelete(false)}
        onConfirm={handleDelete}
        title="Hapus Kajian?"
        message="Apakah Anda yakin ingin menghapus kajian ini? Tindakan ini tidak dapat dibatalkan."
        type="warning"
        confirmText="Ya, Hapus"
        cancelText="Batal"
      />
    </div>
  );
};

export default MaterialDetail;
